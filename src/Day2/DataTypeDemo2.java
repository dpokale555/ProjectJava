package Day2;

//Date type program

public class DataTypeDemo2 {

	public static void main(String[] args) {
		int a=100, b=200;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(a+" "+b);
		System.out.println("The value of a:"+a);
		
		long L=234567898765l;         //for long data type need to add small l or capital L at the end of value
		System.out.println(L);

		int Personal_age=30;
		System.out.println("Person age is:"+Personal_age);
		
		//float item_price=150.31f;   //for float data type need to add small f or capital F at the end of value
		double item_price=150.25;
		System.out.println("Tomato price is:"+item_price);
		
		char grade='A';
		System.out.println("Grade is:"+grade);
		
		boolean status=true;
		System.out.println(status);
		
		
		
	}

}
