package Day2;

public class DataTypeDemo {

	public static void main(String[] args) {
		
		int a;   //this is declaration
		a=100;   // this assignment
		
		//int a=100;  //we can combined both declaration + assignment in one line
		
		System.out.println(a);

		a=200;
		System.out.println(a);
		
		//to represent the multiple variable of same data type in multiple line
		//int b=20;
		//int c=30;
		//int d=40;
		
		//to represent the multiple variable of same data type in single line
		int b=20,c=30,d=40;
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
		
	}

}
